  `arrival_delivery` tinyint(1) NOT NULL,
  `created_at` datetime NOT NULL,
  `updated_at` datetime NOT NULL,
  `created_by` varchar(36) NOT NULL,
  `updated_by` varchar(36) NOT NULL,
  `deleted` tinyint(1) NOT NULL DEFAULT '0',
  `version` int(11) NOT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=3 DEFAULT CHARSET=utf8mb4;

CREATE TABLE `acg_order` (
  `id` varchar(36) NOT NULL,
  `user_id` varchar(36) NOT NULL,
  `user_role` varchar(36) NOT NULL,
  `enterprise_id` varchar(36) DEFAULT NULL,
  `status` int(11) NOT NULL,
  `refund_status` tinyint(1) DEFAULT NULL,
  `vendor` varchar(36) NOT NULL DEFAULT 'none',
  `shipping_info_id` bigint(20) NOT NULL,
  `contact` text NOT NULL,
  `currency` varchar(10) DEFAULT 'CNY',
  `airline_fee` decimal(10,2) NOT NULL,
  `pick_up_fee` decimal(10,2) DEFAULT '0.00',
  `drop_off_fee` decimal(10,2) DEFAULT '0.00',
  `total_fee` decimal(10,2) DEFAULT NULL,
  `delegate_order_id` varchar(36) DEFAULT NULL,
  `delegate_order_pay_id` varchar(36) DEFAULT NULL,
  `cancel_reason` int(11) DEFAULT NULL,
  `created_at` datetime NOT NULL,
  `updated_at` datetime NOT NULL,
  `created_by` varchar(36) NOT NULL,
  `updated_by` varchar(36) NOT NULL,
  `deleted` tinyint(1) NOT NULL DEFAULT '0',
  `version` int(11) NOT NULL,
  `agent_code` varchar(36) DEFAULT NULL,
  `acg_primary_number` varchar(36) DEFAULT NULL,
  PRIMARY KEY (`id`),
  KEY `fk_acg_order_order_status` (`status`),
  KEY `fk_acg_order_acg_shipping_info` (`shipping_info_id`),
  KEY `fk_cancel_reason` (`cancel_reason`),
  KEY `acg_order$user_id` (`user_id`),
  KEY `acg_order$enterprise_id` (`enterprise_id`),
  KEY `acg_order$created_at` (`created_at`),
  KEY `acg_order$deleted` (`deleted`),
  CONSTRAINT `fk_acg_order_acg_shipping_info` FOREIGN KEY (`shipping_info_id`) REFERENCES `acg_shipping_info` (`id`),
  CONSTRAINT `fk_acg_order_order_status` FOREIGN KEY (`status`) REFERENCES `order_status` (`id`),
  CONSTRAINT `fk_cancel_reason` FOREIGN KEY (`cancel_reason`) REFERENCES `cancel_reason` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;


package fun.hercules.order.order.utils;

class CountryInfo {
    private String country;
    private String code;

    public String getCountry() {
        return this.country;
    }

    public String getCode() {
        return this.code;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public void setCode(String code) {
        this.code = code;
    }
}